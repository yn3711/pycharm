from abc import ABCMeta, abstractmethod

class MdlBase(metaclass=ABCMeta):

    ## -----------------------
    ## クラス変数
    ## クラスが持つ変数で、クラスとインスタンス両方で使えます。
    ## -----------------------

    ##=====================================================================================
    ## コンストラクタ
    ## @Parameter:
    ## @Return:
    ## @Note:
    ##=====================================================================================
    #def __init__(self, name):

    ##======================================================================================
    ## テーブル参照
    ## @Parameter:
    ## @Return:
    ## @Note: @abstractmethod PythonでC#やJavaのinterfaceみたいなものを実現する
    ##======================================================================================
    @abstractmethod
    def Mdl_Get(self, soCon):
        pass

    ##======================================================================================
    ## ＳＱＬ実行
    ## @Parameter:
    ## @Return:
    ## @Note:
    ##======================================================================================
    @classmethod   # クラスメソッド
    def Mdl_Execute(self, soCon):
        pass
